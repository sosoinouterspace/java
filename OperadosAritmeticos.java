public class OperadosAritmeticos {
    public static void main(String args[]){
        int v1 = 10;
        int v2 = 20;

        System.out.println("A soma é: "+ (v1+v2));
        System.out.println("A subtração é: "+ (v2-v1));
        System.out.println("A multiplicação é: "+ (v1*v2));
        System.out.println("A divisão é: "+ (v2/v1));
        System.out.println("O módulo é: "+ (v2%v1));
    }
    
}
